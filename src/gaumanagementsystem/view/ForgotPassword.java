    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String username = jTextField1.getText().trim();
        String animal = jTextField2.getText().trim();
        String password = new String(jPasswordField1.getPassword());
        String repeatPassword = new String(jPasswordField2.getPassword());

        if (username.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter username", "Error", JOptionPane.ERROR_MESSAGE);
            jTextField1.requestFocus();
            return;
        }

        if (animal.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your favorite animal", "Error", JOptionPane.ERROR_MESSAGE);
            jTextField2.requestFocus();
            return;
        }

        if (password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter new password", "Error", JOptionPane.ERROR_MESSAGE);
            jPasswordField1.requestFocus();
            return;
        }

        if (repeatPassword.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please repeat the new password", "Error", JOptionPane.ERROR_MESSAGE);
            jPasswordField2.requestFocus();
            return;
        }

        if (!password.equals(repeatPassword)) {
            JOptionPane.showMessageDialog(this, "Passwords do not match", "Error", JOptionPane.ERROR_MESSAGE);
            jPasswordField1.setText("");
            jPasswordField2.setText("");
            jPasswordField1.requestFocus();
            return;
        }

        JOptionPane.showMessageDialog(this,
                "Password reset successful for user: " + username,
                "Success",
                JOptionPane.INFORMATION_MESSAGE);

        // Optionally clear fields
        jTextField1.setText("");
        jTextField2.setText("");
        jPasswordField1.setText("");
        jPasswordField2.setText("");
        jTextField1.requestFocus();
    }//GEN-LAST:event_jButton1ActionPerformed

